#include <bits/stdc++.h>
using namespace std;
#define ENDL '\n'

int main()
{
    for (char i = 'A', j = 'a'; i <= 'Z'; i++, j++)
    {
        cout<< i <<" - ";
        cout<< j <<ENDL;
    }
    return 0;
}